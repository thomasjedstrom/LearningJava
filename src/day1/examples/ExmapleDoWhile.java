package day1.examples;

public class ExmapleDoWhile {

	public static void main(String[] args) {

		int x = 10;
		
		do{
			System.out.println("x = " + x);
			x--;
		}while(x > 0);

		
//		The difference between a "Do While" and a "While" statement is that
//		a Do While statement WILL RUN at least once before checking the conditional
	}

}
